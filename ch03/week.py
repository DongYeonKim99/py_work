day = input("월요일부터 일요일 중 영어로 번역하고 싶은 요일을 입력하세요:")
if day == "월요일":
    print("Monday")
elif day == "화요일":
    print("Tuesday")
elif day == "수요일":
    print("Wednesday")
elif day == "목요일":
    print("Thursday")
elif day == "금요일":
    print("Friday")
elif day == "토요일":
    print("Saturday")
elif day == "일요일":
    print("Sunday")
else:
    print("한글 요일을 잘못 입력했습니다.")